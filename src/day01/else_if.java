package day01;

import java.util.Scanner;

public class else_if {

	public static void main(String[] args) {
		int num =851;
		if (num >100) {
			System.out.println("100보다 크다");
			
		}
		else if(num >80) {
			System.out.println("80보다 크다");
		}
		else if(num >70) {
			System.out.println("70보다 크다");
		}
		else {
			System.out.println("모르겠다");
		}
		System.out.println("다음 문장 시작합니다");
	}//main

} //class
